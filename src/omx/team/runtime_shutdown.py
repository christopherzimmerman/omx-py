def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def _config_is_empty(config: dict[str, Any] | None) -> bool:
    """Read_team_config returns ``{}`` when the file is missing."""
    return not config


def _worker_inbox_path(team_name: str, worker_name: str, cwd: str):
    return _team_dir(team_name, cwd) / "workers" / worker_name / "inbox.md"


def _write_worker_inbox_file(
    team_name: str, worker_name: str, content: str, cwd: str
) -> None:
    """Write the shutdown inbox markdown for a worker. Best-effort."""
    path = _worker_inbox_path(team_name, worker_name, cwd)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def _sync_team_mode_state_on_shutdown(  # noqa: D401 - stub
    team_name: str,
    cwd: str,
    leader_session_id: str | None = None,
) -> None:
    """Best-effort mode-state sync stub.

    Port placeholder for TS ``syncTeamModeStateOnShutdown`` (runtime.ts:254-264).
    The TS implementation rewrites ``.omx/state/team-<sid>.json`` to flip
    ``active=false`` / ``current_phase=cancelled``. The Python
    ``omx.modes.base.update_mode_state`` helper is not yet ported, so this
    is intentionally a no-op. Hooked here so callers and tests can patch
    a single seam once the real implementation lands.

    Never raises — the TS version is best-effort.
    """
    return None


def _send_shutdown_inboxes(
    team_name: str,
    config: dict[str, Any],
    cwd: str,
) -> dict[str, str]:
    """Phase 1: write a shutdown request + inbox for every worker.

    Returns a map of ``worker_name -> requested_at`` so the drain loop can
    pass the minimum-timestamp gate to :func:`read_shutdown_ack`.

    Per-worker errors are swallowed (mirrors TS ``try/catch`` around the
    loop body): we want to make a best-effort attempt for every worker
    even if one of them already disappeared.
    """
    requested_at_by_worker: dict[str, str] = {}
    for worker in config.get("workers") or []:
        name = worker.get("name")
        if not name:
            continue
        try:
            requested_at = _now_iso()
            write_shutdown_request(team_name, name, cwd, requested_by="leader-fixed")
            requested_at_by_worker[name] = requested_at
            inbox = generate_shutdown_inbox(team_name, name)
            _write_worker_inbox_file(team_name, name, inbox, cwd)
        except Exception:  # noqa: BLE001 — TS swallows operation failures
            continue
    return requested_at_by_worker


def _worker_alive(
    session_name: str,
    worker: dict[str, Any],
) -> bool:
    """Return True iff the tmux pane for ``worker`` still has a live process."""
    pane_id = worker.get("pane_id")
    index = int(worker.get("index", 0))
    return is_worker_alive(
        session_name, index, pane_id if isinstance(pane_id, str) else None
    )


def _wait_for_drain(
    team_name: str,
    config: dict[str, Any],
    cwd: str,
    requested_at_by_worker: dict[str, str],
    force: bool,
    drain_timeout_ms: int,
    poll_interval_s: float = 2.0,
) -> list[tuple[str, str]]:
    """Phase 2: poll for ack or pane-gone until ``drain_timeout_ms`` elapses.

    Returns the list of ``(worker_name, reason)`` rejections collected. When
    a reject is observed and ``force=False``, raises ``shutdown_rejected``
    early so the caller can surface the failure without force-killing.
    """
    session_name = str(config.get("tmux_session") or "")
    workers = config.get("workers") or []
    if not workers:
        return []

    deadline_s = time.monotonic() + max(0.0, drain_timeout_ms / 1000.0)
    rejected: list[tuple[str, str]] = []
    acked: set[str] = set()

    while True:
        # Round of ack checks.
        for worker in workers:
            name = worker.get("name")
            if not name:
                continue
            ack = read_shutdown_ack(
                team_name, name, cwd, requested_at_by_worker.get(name)
            )
            if ack is None:
                continue
            if name not in acked:
                acked.add(name)
                try:
                    append_team_event(
                        cwd,
                        TeamEvent(
                            event_type="shutdown_ack",
                            timestamp=_now_iso(),
                            worker_id=name,
                            detail={
                                "reason": (
                                    f"reject:{ack.reason or 'no_reason'}"
                                    if ack.status == "reject"
                                    else "accept"
                                ),
                            },
                        ),
                        team_name,
                    )
                except Exception:  # noqa: BLE001
                    pass
            if ack.status == "reject":
                if not any(r[0] == name for r in rejected):
                    rejected.append((name, ack.reason or "no_reason"))

        if rejected and not force:
            detail = ",".join(f"{w}:{r}" for w, r in rejected)
            raise RuntimeError(f"shutdown_rejected:{detail}")

        any_alive = any(_worker_alive(session_name, w) for w in workers)
        if not any_alive:
            return rejected

        if time.monotonic() >= deadline_s:
            return rejected

        time.sleep(poll_interval_s)


def _teardown_worker_panes(config: dict[str, Any]) -> None:
    """Phase 3: force-kill any worker pane still alive.

    Mirrors the simplified detached-session branch of the TS shutdown.
    Leader and HUD panes are left alone — ``kill_worker`` guards against
    a worker pane id that happens to equal the leader pane.
    """
    session_name = str(config.get("tmux_session") or "")
    leader_pane_id = config.get("leader_pane_id")
    leader_pane = leader_pane_id if isinstance(leader_pane_id, str) else None
    for worker in config.get("workers") or []:
        pane_id = worker.get("pane_id")
        index = int(worker.get("index", 0))
        try:
            kill_worker(
                session_name,
                index,
                pane_id if isinstance(pane_id, str) else None,
                leader_pane,
            )
        except Exception:  # noqa: BLE001 — best-effort teardown
            continue


def _collect_provisioned_worktrees(
    config: dict[str, Any],
) -> list[EnsureWorktreeResult]:
    """Reconstruct ``EnsureWorktreeResult`` entries from the saved config.

    Only workers whose entry has ``worktree_path`` are included. The TS
    helper ``collectProvisionedShutdownWorktrees`` (runtime.ts) walks the
    same field; we materialize a minimal record because the rollback
    helper only consumes ``enabled``, ``created``, ``repo_root``,
    ``worktree_path``, ``branch_name`` and ``created_branch``.
    """
    results: list[EnsureWorktreeResult] = []
    leader_cwd = str(config.get("leader_cwd") or "")
    for worker in config.get("workers") or []:
        worktree_path = worker.get("worktree_path")
        if not isinstance(worktree_path, str) or not worktree_path:
            continue
        repo_root = str(worker.get("repo_root") or leader_cwd)
        if not repo_root:
            continue
        results.append(
            EnsureWorktreeResult(
                enabled=True,
                repo_root=repo_root,
                worktree_path=worktree_path,
                detached=bool(worker.get("worktree_detached", False)),
                branch_name=worker.get("worktree_branch"),
                created=bool(worker.get("worktree_created", True)),
                reused=bool(worker.get("worktree_reused", False)),
                created_branch=bool(worker.get("worktree_branch_created", False)),
            )
        )
    return results


def _cleanup_worktrees(
    config: dict[str, Any],
) -> list[str]:
    """Phase 4: roll back any worker worktrees provisioned at startup.

    Returns a list of cleanup error strings; never raises (callers append
    the errors and decide whether to raise at the end of shutdown).
    """
    errors: list[str] = []
    provisioned = _collect_provisioned_worktrees(config)
    if not provisioned:
        return errors
    try:
        rollback_provisioned_worktrees(
            provisioned,
            RollbackWorktreeOptions(skip_branch_deletion=False),
        )
    except Exception as err:  # noqa: BLE001
        errors.append(f"rollbackProvisionedWorktrees: {err}")
    return errors


def _remove_worktree_root_agents(team_name: str, config: dict[str, Any]) -> None:
    """Remove worker worktree-root AGENTS.md installs. Idempotent."""
    for worker in config.get("workers") or []:
        worktree_path = worker.get("worktree_path")
        team_state_root = worker.get("team_state_root")
        name = worker.get("name")
        if not (
            isinstance(worktree_path, str)
            and isinstance(team_state_root, str)
            and isinstance(name, str)
            and worktree_path
            and team_state_root
        ):
            continue
        try:
            remove_worker_worktree_root_agents_file(
                team_name, name, team_state_root, worktree_path
            )
        except Exception:  # noqa: BLE001 — best-effort
            continue


def _terminal_phase_for_shutdown(
    rejected: list[tuple[str, str]],
    force: bool,
    confirm_issues: bool,
) -> str:
    """Pick the terminal phase to record on shutdown.

    - Rejections (or a force shutdown) collapse to ``cancelled``.
    - Otherwise the terminal phase is ``complete``.

    ``confirm_issues`` does not change the terminal phase by itself — it
    only relaxes the cleanup gate.
    """
    if rejected or force:
        return "cancelled"
    return "complete"


