def shutdown_team(
    team_name: str,
    cwd: str,
    options: ShutdownOptions | None = None,
    *,
    drain_timeout_ms: int = 15_000,
    poll_interval_s: float = 2.0,
) -> TeamShutdownSummary:
    """Gracefully shut down a team and tear down its tmux + worktree state.

    Args:
        team_name: Caller-supplied team name. Sanitized before use.
        cwd: Working directory rooting the team's ``.omx/team`` state.
        options: Optional ``ShutdownOptions``. ``force=True`` skips the
            ack/reject gate and force-kills uncooperative workers.
            ``confirm_issues=True`` permits final state cleanup even when
            rejections were observed.
        drain_timeout_ms: Wait this long (ms) for workers to drain. The
            TS default is 15 000.
        poll_interval_s: Sleep this long between drain polls.

    Returns:
        :class:`TeamShutdownSummary`. ``commit_hygiene_artifacts`` is
        always ``None`` in this port — commit hygiene is run by a TS
        pipeline that has not been ported yet.

    Raises:
        RuntimeError: When ``force=False`` and at least one worker rejects
            the shutdown (``shutdown_rejected:<worker>:<reason>``), or when
            non-recoverable cleanup errors occurred after the panes were
            torn down.
    """
    opts = options or ShutdownOptions()
    force = opts.force
    confirm_issues = opts.confirm_issues

    sanitized = sanitize_team_name(team_name)
    # ``team_read_config`` is a direct re-export of ``state.io.read_team_config``
    # which takes ``(cwd, team_name)`` — it does not follow the (team_name, cwd)
    # convention documented on the team_ops gateway.
    config = team_read_config(cwd, sanitized)

    # Empty config: best-effort tmux session destroy + state cleanup.
    if _config_is_empty(config):
        try:
            destroy_team_session(f"omx-team-{sanitized}")
        except Exception:  # noqa: BLE001
            pass
        try:
            team_cleanup(cwd, sanitized)
        except Exception:  # noqa: BLE001
            pass
        _sync_team_mode_state_on_shutdown(sanitized, cwd)
        return TeamShutdownSummary(commit_hygiene_artifacts=None)

    manifest = team_read_manifest(sanitized, cwd)
    leader_session_id = ""
    if manifest is not None:
        raw_session_id = (
            getattr(getattr(manifest, "leader", None), "session_id", "") or ""
        )
        if isinstance(raw_session_id, str):
            leader_session_id = raw_session_id.strip()

    # Phase 1: send shutdown inboxes and request handshakes.
    requested_at_by_worker = _send_shutdown_inboxes(sanitized, config, cwd)

    # Phase 2: poll for drain. Raises ``shutdown_rejected`` when a worker
    # rejects and ``force`` is not set.
    rejected = _wait_for_drain(
        sanitized,
        config,
        cwd,
        requested_at_by_worker,
        force=force,
        drain_timeout_ms=drain_timeout_ms,
        poll_interval_s=poll_interval_s,
    )

    # Phase 3: force-kill remaining panes (always — even on graceful
    # shutdown, a Codex worker may have acked but not exited).
    _teardown_worker_panes(config)

    # Best-effort kill of the tmux session itself for non-shared sessions.
    session_name = str(config.get("tmux_session") or "")
    if session_name and ":" not in session_name:
        try:
            destroy_team_session(session_name)
        except Exception:  # noqa: BLE001
            pass

    # Phase 4: rollback worktrees + cleanup worker overlays / instructions.
    cleanup_errors: list[str] = []
    _remove_worktree_root_agents(sanitized, config)
    try:
        remove_team_worker_instructions_file(sanitized, cwd)
    except Exception:  # noqa: BLE001
        pass

    cleanup_errors.extend(_cleanup_worktrees(config))

    # Phase 5: write the final phase + mark leader stopped.
    terminal_phase = _terminal_phase_for_shutdown(rejected, force, confirm_issues)
    try:
        write_phase_state(
            _team_dir(sanitized, cwd),
            TeamPhaseState(current_phase=terminal_phase, updated_at=_now_iso()),
        )
    except Exception:  # noqa: BLE001
        pass
    try:
        mark_team_leader_session_stopped(sanitized, cwd, leader_session_id)
    except Exception:  # noqa: BLE001
        pass

    # Phase 6: cleanup team state directory.
    #
    # TS gates this on the classify-shutdown gate; in the simplified port
    # we cleanup unconditionally on success, and only when
    # ``confirm_issues=True`` if any rejection was observed. Force always
    # cleans up.
    can_cleanup = force or confirm_issues or not rejected
    team_state_cleaned = False
    if can_cleanup:
        try:
            team_cleanup(cwd, sanitized)
            team_state_cleaned = True
        except Exception as err:  # noqa: BLE001
            cleanup_errors.append(f"cleanupTeamState: {err}")
    if team_state_cleaned:
        _sync_team_mode_state_on_shutdown(sanitized, cwd, leader_session_id)

    if cleanup_errors:
        raise RuntimeError(" | ".join(cleanup_errors))

    return TeamShutdownSummary(commit_hygiene_artifacts=None)
