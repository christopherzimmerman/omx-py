"""Team runtime: graceful shutdown entry point.

Port of ``shutdownTeam`` from ``src/team/runtime.ts`` (lines 2961-3310).
Phase 2.9b — sync-only, stdlib-only.

High-level flow (mirrors TS step-for-step, simplified for ported surface):

1. Sanitize ``team_name`` and read the config + manifest.
2. If no config, best-effort kill the tmux session and cleanup state.
3. **Send shutdown inboxes** to every worker via
   :func:`omx.team.state.shutdown.write_shutdown_request` plus a worker
   inbox file generated by
   :func:`omx.team.worker_bootstrap.generate_shutdown_inbox`.
4. **Wait for drain.** Poll up to ``drain_timeout_ms`` (default 15 000 ms,
   matching TS) for every worker to either ack the shutdown or have its
   pane go away. Sleep 2 s between rounds.
5. On any ``reject`` ack, raise ``shutdown_rejected`` unless ``force=True``.
6. **Force kill** remaining workers using
   :func:`omx.team.tmux_session.kill_worker`.
7. **Tear down** the tmux session (best-effort).
8. **Roll back** any worker worktrees via
   :func:`omx.team.worktree.rollback_provisioned_worktrees`.
9. **Cleanup** worktree-root AGENTS.md installs and the team worker
   instructions file.
10. **Write the final phase** as ``cancelled`` (or ``complete`` when no
    rejections happened), mark the leader session stopped, and call
    :func:`omx.team.team_ops.team_cleanup` to delete the team state dir
    (when ``confirm_issues=True`` or no failures were observed).

Simplifications relative to TS:

- **Mode-state sync** (``syncExactTeamModeStateOnShutdown``,
  ``matchesTeamModeStateForShutdown``) is stubbed. ``omx.modes.base.update_mode_state``
  is not yet ported (tracked separately). The stub is documented and
  intentionally never raises.
- **Shutdown gate / classification** (``classifyShutdown`` /
  ``shutdown_gate_blocked`` errors) is collapsed to the ``force`` flag
  and the per-worker ack check. The richer gate that inspects task
  counts will land alongside ``classifyShutdown`` in a later phase.
- **Commit hygiene** (``prepareWorkerWorktreeShutdownReports`` plus the
  ledger / artifact writers) is not run; the summary always returns
  ``commit_hygiene_artifacts=None``. Hygiene artifacts come from a TS
  pipeline that has not been ported yet.
- **Shared-session topology** (``resolveSharedSessionShutdownTopology``,
  ``teardownWorkerPanes`` async primitives) is folded into the simpler
  per-worker ``kill_worker`` loop, which is correct for the
  detached-session case the Python runtime exercises today.

The public API:

    shutdown_team(team_name: str, cwd: str,
                  options: ShutdownOptions | None = None,
                  *, drain_timeout_ms: int = 15_000) -> TeamShutdownSummary
"""

